
function iniciar(){
var iniciar=document.getElementById("login-form");
var registrar=document.getElementById("register-form");
iniciar.style.display = "block";
registrar.style.display = "none";
}
function limpiar(){
    var iniciar=document.getElementById("login-form");
    var registrar=document.getElementById("register-form");
    iniciar.style.display = "none";
    registrar.style.display = "block";
}

